vi primes;

inline void Sieve (ll MAXN) {
	bool isPrime[MAXN];
	memset(isPrime, 1, sizeof(isPrime));
	isPrime[0] = isPrime[1] = 0;
	for (int i=2; i<=MAXN; i++) {
		if (isPrime[i]) {
			for (int j=i*2; j<=MAXN; j+=i) isPrime[j] = 0;
			primes.pb(i);
		}
	}
}

